return {
  "zbirenbaum/copilot.lua",
  cmd = "Copilot",
  event = "InsertEnter",
  config = function()
    require("copilot").setup({
      suggestion = {
        enabled = true,
        auto_trigger = true,
        hide_during_completion = true,
        debounce = 75,
        keymap = {
          accept = "<M-l>", -- Alt+l to accept
          accept_word = "<M-w>", -- Alt+w to accept word
          accept_line = "<M-j>", -- Alt+j to accept line
          next = "<M-]>", -- Alt+] for next suggestion
          prev = "<M-[>", -- Alt+[ for previous suggestion
          dismiss = "<C-]>", -- Ctrl+] to dismiss
        },
      },
      panel = {
        enabled = true,
        auto_refresh = true,
        keymap = {
          jump_prev = "[[",
          jump_next = "]]",
          accept = "<CR>",
          refresh = "gr",
          open = "<M-CR>",
        },
      },
      filetypes = {
        yaml = false,
        markdown = false,
        help = false,
        gitcommit = false,
        gitrebase = false,
        hgcommit = false,
        svn = false,
        cvs = false,
        ["."] = false,
      },
    })
  end,
}
